void oled_write_char(char c)
{
    for (int i = 0; i < 5; i++)
    {
        oled_data(font5x7[(int)c][i]);
    }

    oled_data(0x00);
}

void oled_write_string(const char *str)
{
    while (*str)
    {
        oled_write_char(*str++);
    }
}

void oled_print(uint8_t page, uint8_t col, const char *str)
{
    oled_set_cursor(page, col);
    oled_write_string(str);
}

void oled_print_float(uint8_t page,
                      uint8_t col,
                      float value,
                      uint8_t decimals)
{
    char buffer[32];

    sprintf(buffer, "%.*f", decimals, value);

    oled_print(page, col, buffer);
}